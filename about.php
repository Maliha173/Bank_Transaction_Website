<b>About us:
Islami Bank Bangladesh PLC. (IBBPLC.) is an Islamic banking company based in Bangladesh. It became incorporated on 13 March 1983 as a public limited company under the Companies Act 1913 and started operation on 30 March, the same year.[7] IBBPLC maintains its own ATM Booths with a country-wide shared ATM network. IBBPLC has been the largest private banking network in Bangladesh.[8] When IBBPLC was established, it was the first bank in Southeast Asia to provide banking service based on Shariah.[9] The bank has been listed with both Dhaka Stock Exchange Ltd. and Chittagong Stock Exchange Ltd.[7][10]. Bangladesh became a member of the Organisation of Islamic Cooperation (OIC) in February 1974 and signed the charter for Islamic Development Bank in September that year. In April 1978, the 9th conference of OIC foreign ministers recommended for reorganizing the banking system of the member countries to Islamic operations systems gradually. In November 1980, A S M Fakhrul Ahsan, Director (Research) of Bangladesh Bank recommended for commencing Islamic banking operation in Bangladesh. In April 1981, the Ministry of Finance in a letter written to Bangladesh Bank recommended to start separate Islamic banking counters in all branches of the state-owned banks of Bangladesh on an experimental basis and keep separate ledgers for this purpose. In November 1982, a delegation of the Islamic Development Bank visited Dhaka to monitor and evaluate the possibility of a joint venture to establish an Islamic bank in the private sector in Bangladesh and recommended the Islamic Development Bank as an entrepreneur to invest capital in the proposed Islamic bank. As a consequence, Islami Bank Bangladesh Limited, the first shari'ah compliant bank in Bangladesh started operations on 30 March 1983.[11] It was founded by Saudi and Kuwaiti investors who provided 70 per cent of the initial capital.[6][11] It is a public limited company registered under the Indian Companies Act of 1913. IBBL is a joint venture of the government of Bangladesh, 22 businessmen of Bangladesh, Islamic Development Bank, and investment firms and banks from Muslim Middle Eastern countries.[12]

According to The Economist, "Islami Bank was a pioneer in financing Bangladesh’s rise as the apparel industry’s main production base outside China."[6]

In 2012, United States Senate Permanent Subcommittee on Investigations mentioned possible ties of Islami Bank Bangladesh Limited to terrorism financing through HSBC Bank which the bank denied.[13][14][15][16] The report questioned the banks link to Al Rajhi Bank.[16] Islami Bank hired Dana Contratto and Associates, a lobbying company, for 25 thousand USD per month to lobby for the bank in Washington DC.[17]

In July 2013, Islami Bank had difficulties getting repayment from Ananda Shipyard & Slipways Limited after the company lost 89.4 million USD foreign orders.[18]

Clients of JP Morgan purchased a three percent stake in the Islami Bank in November 2015.
